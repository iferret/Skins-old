import Foundation
import UIKit.UISegmentedControl

extension SkinsWrapper where Base: UISegmentedControl {
    
    /// selectedSegmentTintColor
    @available(iOS 13.0, *)
    public var selectedSegmentTintColor: SKColor? {
        get { Skins.shared.action(forKey: .init(string: #function), target: base)?.color }
        set {
            let action = SKAction.init(entity: .color(newValue, {[weak base] (style, color) in
                base?.selectedSegmentTintColor = color?.current(with: style)
            })).run()
            Skins.shared.add(action: action, forKey: .init(string: #function), target: base)
        }
    }
}
